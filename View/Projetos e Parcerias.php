			<h1 id='TRABALHO_AREA_EDUCACAO' class="titulo" style="font-size: 2em; margin: 20px;">Administração da Escola Municipal de Educação Infantil Leonildo Braym (2019-Atual)</h1>
			<hr class="linhaPostTitulo">
			
			
			
			
			<p class="textoPNoticia">No mês de março, a Prefeitura de Carapicuíba realizou várias inaugurações em comemoração aos 54 anos da cidade. Entre elas, através da Secretaria de Educação, esteve a entrega da EMEI Leonildo Braym, que aconteceu no dia 24 de março. Além da nova estrutura, a escola iniciou suas atividades com um novo modelo administrativo, em parceria com o Instituto Ânima.</p>
			<figure style='width:100%; margin-top: 20px;'>
				<img class="imagemConig" src="<?php echo $localURL;?>imgs/conchecainstituto/Fotoemilio1.jpg">
				<img class="imagemConig" src="<?php echo $localURL;?>imgs/conchecainstituto/Fotoemilio2.jpg">
