			</figure>
			<br style='clear:both;'/>
			<br/>

			<p class="textoPNoticia">Com capacidade para atender 200 alunos, a escola está localizada no bairro Jardim Novo Horizonte e possui 4 salas de aula, cozinha, refeitório, dispensa, dml, solário, pátio coberto, playground e sistema de segurança (24h).</p>

			<p class="textoPNoticia">Com um projeto pioneiro na cidade, a EMEI Leonildo Braym é a primeira escola terceirizada de Carapicuíba. Implementar a terceirização foi uma decisão estratégica para enxugar os custos e elevar a qualidade do ensino, seguindo o exemplo de outras cidades da região, como Barueri, Osasco e São Paulo que já utilizam o modelo há mais tempo.</p>

			<p class="textoPNoticia">Após um rigoroso processo de seleção, realizado por meio de um chamamento público, Organizações Sociais apresentaram propostas para a gestão pedagógico-financeira da escola. Tendo apresentado a melhor proposta, venceu o Instituto Ânima, que ficou responsável por realizar o gerenciamento e operacionalização da escola. O projeto pedagógico implementado segue as diretrizes da Secretaria de Educação de Carapicuíba e está e consonância com os critérios da Base Nacional Comum Curricular - BNCC.</p>

			<p class="textoPNoticia">Com experiência em administração de escolas na cidade de Barueri, o Instituto Ânima vem somar esforços com a Prefeitura de Carapicuíba sob a supervisão direta da Secretaria de Educação e de Finanças de Carapicuíba, além de prestar contas ao Tribunal de Contas do Estado – TCE.</p>

