			<hr class="linhaPostTitulo">
			
			<p style='font-weight:bold;'>Cursos e Atividades para a população carente</p>
			
			<p class="textoPNoticia">Preocupados com o alto índice de evasão escolar, baixa escolaridade e com o número cada vez maior de crianças e adolescentes envolvidos com drogas, além da alta vulnerabilidade no que diz respeito à proteção integral dos direitos da criança e do adolescente, nos anos de 2011 e 2012, o Instituto Ânima atuou com oficinas para crianças e pré-adolescentes de 03 a 15 anos no contra turno escolar. As atividades contribuíram para o desenvolvimento do protagonismo e autonomia por meio de atividades lúdicas e culturais como forma de expressão, interação e aprendizagem.</p>
